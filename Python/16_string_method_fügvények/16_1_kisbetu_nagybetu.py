a = "árvíztűrő tükörfúrógép"

print(a.capitalize())
#Csak karakter lánc első betűje nagybetű
print(a.lower())
print(a.casefold())
#A karakterek csupa kisbetű
print(a.upper())
#A karakterek csupa nagybetűsek
print(a.swapcase())
#Felcseréli a kis és nagybetűket!
print(a.title())
